package com.capgemini.javademo;

//class name IfTF declared
public class IfTF {

	//main method started
	public static void main(String[] args) {
		int x = 8, y = 7; //values of x and y is declared
		
        if(x >= y) //if statement
        {
     	   System.out.println("true");
        }
        else //else statement
        {
     	   System.out.println("false");
        }

	}

}
