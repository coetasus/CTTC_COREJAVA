package com.capgemini.javademo;

//class name Forloop is declared
public class ForLoop {

	//main method is declared
	public static void main(String[] args) {
		//for loop execution started
		for(int i = 1; i<=10 ; i++)  
		{	
			System.out.print("Value of i:");
			System.out.println(i);
			//value of i 1-10 will be printed

	}
        for(int i=10; i>=1; i--)
        {
        	System.out.println("Value of i in reverse:");
        	System.out.println(i);
        	//value of i 10-1 will be printed
        }
}}
