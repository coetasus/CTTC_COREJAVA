//18 printing value using while loop
package com.capgemini.javademo;

//class name Whileloop declared
public class WhileLoop {

	//main method is declared
	public static void main(String[] args) {
		//value of i=99 is given
		int i=98;
		
		while(i<=100)
		{
			//value of i will printed
			System.out.println(i);
			//value of i increment by 1
			i++;
		}


	}

}
