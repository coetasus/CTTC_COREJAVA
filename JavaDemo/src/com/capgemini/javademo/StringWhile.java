//19 printing value using do while loop
package com.capgemini.javademo;

//class name StringWhile is declared
public class StringWhile {
	//main method is declared
	public static void main(String[] args) {
		
	
	int i =11;
	//do while loop started
	do
	{
		System.out.println(i);
		i++;
	}while(i<=1);

}
}